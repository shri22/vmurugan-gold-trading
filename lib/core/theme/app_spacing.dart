/// App spacing constants for consistent UI layout
class AppSpacing {
  // Base spacing unit
  static const double unit = 4.0;
  
  // Spacing sizes
  static const double xs = unit; // 4.0
  static const double sm = unit * 2; // 8.0
  static const double md = unit * 4; // 16.0
  static const double lg = unit * 6; // 24.0
  static const double xl = unit * 8; // 32.0
  static const double xxl = unit * 12; // 48.0
  
  // Specific use cases
  static const double cardPadding = md;
  static const double screenPadding = md;
  static const double buttonHeight = 48.0;
  static const double inputHeight = 56.0;
  static const double iconSize = 24.0;
  static const double borderRadius = 8.0;
  static const double cardElevation = 2.0;
}
